    private void CBProizvodi() {
       try {
            cmbProizvodi.removeAllItems();
            List<Proizvod> proizvodi = Controller.getInstance().vratiProizvode();
            for (Proizvod p : proizvodi) {
                cmbProizvodi.addItem(p);
            }
        } catch (Exception ex) {
            Logger.getLogger(CenaInsert.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void CBDobavljaci() {
       try {
            cmbDobavljaci.removeAllItems();
            List<Dobavljac> dobavljaci = Controller.getInstance().vratiDobavljace();
            for (Dobavljac d : dobavljaci) {
                cmbDobavljaci.addItem(d);
            }
        } catch (Exception ex) {
            Logger.getLogger(CenaInsert.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
