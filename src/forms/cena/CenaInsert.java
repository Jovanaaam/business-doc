    private void btnUbaciActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbaciActionPerformed
        try {
            Dobavljac d = (Dobavljac) cmbDobavljaci.getSelectedItem();
            Proizvod p = (Proizvod) cmbProizvodi.getSelectedItem();
            
            DateFormat df = new SimpleDateFormat("dd.MM.yyyy");
            
            Date datumpromenecene = df.parse(txtDatumPromeneCene.getText().trim());
            
            
            double nabavnacena = Double.parseDouble(txtNabavnaCena.getText());
            
           
                
                Cena c = new Cena(d, datumpromenecene, p, "", nabavnacena);
               boolean provera = Controller.getInstance().proveriDaLiVecPostojiDatum(c);
              if(provera){
              Controller.getInstance().sacuvajCenu(c);
                JOptionPane.showMessageDialog(this, "Cena je uspesno sacuvana!");
              }
              else{
                   JOptionPane.showMessageDialog(this, "Datum za izabrani proizvod i dobavljaca vec postoji PK!!!");
              }
                
               
           
        } catch (ParseException ex) {
            Logger.getLogger(CenaInsert.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            Logger.getLogger(CenaInsert.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "GRESKA!");
              // JOptionPane.showMessageDialog(this, "Sistem ne može da sačuva podatke o ceni.", "Greška", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_btnUbaciActionPerformed

