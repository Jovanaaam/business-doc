    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed
        try {
            DateFormat df = new SimpleDateFormat("dd.MM.yyyy");
            Date datumprometa = df.parse(txtDatumprometa.getText().trim());
            Date datumfak = df.parse(txtDatumfakturisanja.getText().trim());
            StopaPDV s = (StopaPDV) cmbStope.getSelectedItem();
            Zaposleni z = (Zaposleni) cmbZaposleni.getSelectedItem();
            OdgovornoLice o = (OdgovornoLice) cmbOdgLica.getSelectedItem();
            
            
            Faktura f = new Faktura(datumfak, datumprometa, s, z, o, 0);
            Controller.getInstance().sacuvajFakturu(f);
             JOptionPane.showMessageDialog(this, "Uspesan unos fakture");
             this.dispose();
        } catch (ParseException ex) {
            Logger.getLogger(FakturaInsert.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            Logger.getLogger(FakturaInsert.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnIzmeniActionPerformed

