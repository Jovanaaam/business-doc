    private void CBStope() {
        try{
            cmbStope.removeAllItems();
            List<StopaPDV> stope = Controller.getInstance().vratiStopePDV();
            
            for (StopaPDV s : stope) {
                  System.out.println(s + "ddddddddddddddddd");
                cmbStope.addItem(s );
              
                
    
    }
     } catch (Exception ex) {
             Logger.getLogger(FakturaUpdate.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void CBOdgLica() {
         try{
            cmbOdgLica.removeAllItems();
            List<OdgovornoLice> odgLica = Controller.getInstance().vratiOdgLicaSvihDobavljaca();
            
            for (OdgovornoLice ol : odgLica) {
                cmbOdgLica.addItem(ol);
    
    }
     } catch (Exception ex) {
            System.out.println("Greska prilikom vracanja liste stp. pdv");
            System.out.println(ex);
        }
    }

    private void CBZaposleni() {
      try{
            cmbZaposleni.removeAllItems();
            List<Zaposleni> zaposleni = Controller.getInstance().vratiZaposleneView();
            
            for (Zaposleni z : zaposleni) {
                cmbZaposleni.addItem(z);
    
    }
     } catch (Exception ex) {
            System.out.println("Greska prilikom vracanja liste zaposlenih");
            System.out.println(ex);
        }
    }
